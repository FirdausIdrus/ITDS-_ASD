st.markdown("## What is Autism")
st.markdown("""
Autism is a complex neurodevelopmental condition characterized by differences in communication, behavior, and social interaction. It is a lifelong condition that appears in early childhood, typically before age 3. Individuals with autism may experience challenges in understanding social cues, developing language skills, and adapting to changes in routine. The severity of autism can vary widely, with some individuals living independently while others may require significant support throughout their lives.

""")

st.markdown("## What is Autism Spectrum Disorder (ASD)")
st.markdown("""
Autism Spectrum Disorder (ASD) encompasses a range of neurodevelopmental conditions that affect an individual's behavior, communication, and social skills. The term "spectrum" reflects the wide variation in challenges and strengths possessed by each person with autism. ASD is typically diagnosed based on criteria outlined in the Diagnostic and Statistical Manual of Mental Disorders (DSM-5). The spectrum includes conditions previously considered separate, such as Asperger syndrome and pervasive developmental disorder-not otherwise specified (PDD-NOS). Individuals with ASD may have unique ways of learning, paying attention, or reacting to different sensory experiences.

""")

st.markdown("## Signs and Symptoms of Austism")
st.markdown("""
The signs and symptoms of ASD can vary greatly from one individual to another but often include:

- Social Communication Challenges: Difficulty in understanding and using verbal and non-verbal communication, trouble with initiating or maintaining conversations, and limited eye contact or facial expressions.
- Repetitive Behaviors: Engaging in repetitive movements or speech patterns, insistence on sameness, strict adherence to routines, and intense focus on specific interests.
- Sensory Sensitivities: Unusual reactions to sensory inputs, such as being overly sensitive or under-sensitive to lights, sounds, textures, or smells.
- Developmental Delays: Delays in reaching language, cognitive, or motor milestones compared to peers.
""")

st.markdown("## Early Detection and Diagnosis")
st.markdown("""
Early detection and diagnosis of ASD are crucial for effective intervention and support. Key steps in the process include:

- Developmental Screening: Regular screening by pediatricians during routine check-ups to monitor developmental milestones and identify potential signs of ASD.
- Comprehensive Diagnostic Evaluation: If concerns are identified, a detailed evaluation is conducted by a team of specialists, including developmental pediatricians, psychologists, neurologists, and speech-language pathologists. This evaluation includes a thorough review of the child's behavior, communication, and developmental history.
- Diagnostic Tools: Use of standardized tools and assessments such as the Autism Diagnostic Observation Schedule (ADOS) and the Autism Diagnostic Interview-Revised (ADI-R) to support the diagnosis.
""")

st.markdown("## Treatment and Support")
st.markdown("""
While there is no cure for ASD, various treatments and supports can help individuals manage symptoms and lead fulfilling lives. Key approaches include:

- Behavioral Interventions: Applied Behavior Analysis (ABA) is a widely used approach that helps improve social, communication, and learning skills through positive reinforcement.
- Speech and Language Therapy: Helps individuals develop communication skills, ranging from verbal speech to alternative communication methods such as sign language or communication devices.
- Occupational Therapy: Focuses on improving daily living skills, motor skills, and sensory integration.
- Educational Support: Individualized Education Programs (IEPs) and specialized educational plans to address the unique learning needs of children with ASD.
- Medications: In some cases, medications may be prescribed to manage co-occurring conditions such as anxiety, depression, or attention deficit hyperactivity disorder (ADHD).
""")


